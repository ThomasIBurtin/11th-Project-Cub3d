#include "../includes/libft.h"
#include "../includes/printf.h"

void	ft_bzero(void *s, size_t n)
{
	size_t			i;
	unsigned char	*ptr;

	i = 0;
	ptr = s;
	while (i++ < n)
	{
		*ptr++ = 0;
	}
}
